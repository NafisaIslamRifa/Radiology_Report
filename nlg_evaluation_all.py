# ==========================================
# 4. MODEL
# ==========================================
class CheXbertLabeler(nn.Module):
    def __init__(self):
        super().__init__()
        self.bert = BertModel.from_pretrained("bert-base-uncased")
        hidden = self.bert.config.hidden_size
        self.heads = nn.ModuleList([nn.Linear(hidden, 4) for _ in range(14)])

    def forward(self, input_ids, attention_mask):
        out = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        cls = out.last_hidden_state[:, 0]
        logits = [h(cls) for h in self.heads]
        return torch.stack(logits, dim=1)   # [B, 14, 4]

# ==========================================
# 5. SAFE WEIGHT LOADING (FIXED)
# ==========================================
def load_chexbert(weights_path):
    model = CheXbertLabeler().to(device)

    if not os.path.exists(weights_path):
        raise FileNotFoundError(f"Missing: {weights_path}")

    state = torch.load(weights_path, map_location=device)

    # unwrap nested dicts if needed
    if isinstance(state, dict):
        for k in ["state_dict", "model_state_dict", "model"]:
            if k in state:
                state = state[k]
                break

    # remove "module." prefix (VERY IMPORTANT FIX)
    new_state = {}
    for k, v in state.items():
        new_state[k.replace("module.", "")] = v

    model.load_state_dict(new_state, strict=False)

    model.eval()
    print("✅ CheXbert loaded successfully")
    return model

# ==========================================
# 6. LABEL EXTRACTION
# ==========================================
def get_labels(model, tokenizer, texts, batch_size=16):
    preds_all = []

    for i in tqdm(range(0, len(texts), batch_size), desc="Labeling"):
        batch = texts[i:i+batch_size]

        inputs = tokenizer(
            batch,
            padding=True,
            truncation=True,
            max_length=512,
            return_tensors="pt"
        ).to(device)

        with torch.no_grad():
            logits = model(inputs.input_ids, inputs.attention_mask)

        preds = torch.argmax(logits, dim=2).cpu().numpy()
        preds_all.append(preds)

    return np.vstack(preds_all)

# ==========================================
# 7. EVALUATION (FIXED BINARY LOGIC)
# ==========================================
def evaluate(y_true, y_pred):
    results = []

    for i, c in enumerate(CHEXBERT_CONDITIONS):
        yt = y_true[:, i]
        yp = y_pred[:, i]

        # FIX: convert to binary classification
        yt_bin = (yt == 1).astype(int)
        yp_bin = (yp == 1).astype(int)

        results.append({
            "Condition": c,
            "Precision": precision_score(yt_bin, yp_bin, zero_division=0),
            "Recall": recall_score(yt_bin, yp_bin, zero_division=0),
            "F1": f1_score(yt_bin, yp_bin, zero_division=0),
            "Support": int(yt_bin.sum())
        })

    df = pd.DataFrame(results)

    # macro average
    df.loc[len(df)] = {
        "Condition": "MACRO AVG",
        "Precision": df["Precision"].mean(),
        "Recall": df["Recall"].mean(),
        "F1": df["F1"].mean(),
        "Support": df["Support"].sum()
    }

    return df

