def run_nih_localization(max_images=None, score_cam=True):
    boxes = load_nih_boxes(NIH_BBOX_CSV)
    if max_images is not None:
        boxes = boxes.iloc[:max_images]

    rows, missing = [], 0
    for r in tqdm(boxes.itertuples(index=False), total=len(boxes),
                  desc="NIH transfer eval"):
        img_path = os.path.join(NIH_IMG_DIR, r.fname)
        if not os.path.exists(img_path):
            missing += 1
            continue
        image = Image.open(img_path).convert("RGB")
        orig_wh = image.size                       # (W, H)
        prompt = get_random_clinical_prompt()

        maps = {
            "Ours":     vlm_cross_attention(image),
            "Grad-CAM": vlm_gradcam(image, prompt),
            "HiResCAM": vlm_hirescam(image, prompt),
        }
        if score_cam:
            maps["Score-CAM"] = vlm_scorecam(image, prompt)

        target_wh = (maps["Ours"].shape[1], maps["Ours"].shape[0])  # (W,H)
        box = rescale_box((r.bx, r.by, r.bw, r.bh), orig_wh, target_wh)

        for method, hm in maps.items():
            rows.append({
                "image": r.fname, "finding": r.label, "method": method,
                "pointing_game": pointing_game(hm, box),
                "iou": iou_at_threshold(hm, box, thr=0.5),
            })

    if missing:
        print(f"[warn] {missing} images listed in CSV not found in {NIH_IMG_DIR}")
    return pd.DataFrame(rows)


def summarise_nih(df):
    g = df.groupby("method").agg(
        pointing_game=("pointing_game", "mean"),
        iou=("iou", "mean"),
        n=("pointing_game", "size"),
    )
    order = [m for m in ["Grad-CAM", "HiResCAM", "Score-CAM", "Ours"]
             if m in g.index]
    return g.loc[order]


def nih_to_latex(summary):
    lines = [r"\begin{tabular}{lccc}", r"\toprule",
             r"Method & Pointing Game $\uparrow$ & IoU@0.5 $\uparrow$ & $n$ \\",
             r"\midrule"]
    for m in summary.index:
        row = summary.loc[m]
        name = r"\textbf{Cross-Attention (Ours)}" if m == "Ours" else m
        lines.append(f"{name} & {row.pointing_game:.3f} & "
                     f"{row.iou:.3f} & {int(row.n)} \\\\")
    lines += [r"\bottomrule", r"\end{tabular}"]
    return "\n".join(lines)


