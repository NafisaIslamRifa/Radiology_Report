def load_nih_boxes(csv_path):
    df = pd.read_csv(csv_path)
    df.columns = [c.strip() for c in df.columns]
    # find the filename column (it is literally "Image Index")
    fname_col = [c for c in df.columns if "Image" in c][0]
    label_col = [c for c in df.columns if "Finding" in c][0]
    # the four geometry columns, in order, are everything else minus any
    # trailing unnamed columns NIH sometimes appends
    geo = [c for c in df.columns
           if c not in (fname_col, label_col) and not c.startswith("Unnamed")]
    geo = geo[:4]
    out = pd.DataFrame({
        "fname": df[fname_col].astype(str),
        "label": df[label_col].astype(str),
        "bx": pd.to_numeric(df[geo[0]], errors="coerce"),
        "by": pd.to_numeric(df[geo[1]], errors="coerce"),
        "bw": pd.to_numeric(df[geo[2]], errors="coerce"),
        "bh": pd.to_numeric(df[geo[3]], errors="coerce"),
    }).dropna(subset=["bx", "by", "bw", "bh"])
    return out


