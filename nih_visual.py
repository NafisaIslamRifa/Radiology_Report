def pointing_game(heatmap, box):
    y, x = np.unravel_index(np.argmax(heatmap), heatmap.shape)
    x0, y0, x1, y1 = box
    return float((x0 <= x <= x1) and (y0 <= y <= y1))

def iou_at_threshold(heatmap, box, thr=0.5):
    hm = heatmap.astype(np.float32)
    hm = (hm - hm.min()) / (hm.max() - hm.min() + 1e-8)
    pred = hm >= thr
    gt = np.zeros_like(pred, dtype=bool)
    x0, y0, x1, y1 = [int(v) for v in box]
    gt[max(0,y0):max(0,y1), max(0,x0):max(0,x1)] = True
    inter = np.logical_and(pred, gt).sum()
    union = np.logical_or(pred, gt).sum()
    return float(inter) / float(union + 1e-8)

def rescale_box(box_xywh, orig_wh, target_wh):
    x, y, w, h = box_xywh
    ow, oh = orig_wh
    tw, th = target_wh
    sx, sy = tw / ow, th / oh
    return (x*sx, y*sy, (x+w)*sx, (y+h)*sy)


